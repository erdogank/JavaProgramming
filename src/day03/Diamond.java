public class Diamond {
    public static void main(String[] args) {

        System.out.println("\t\t    ^");
        System.out.println("\t\t   / \\");
        System.out.println("\t\t  / * \\");
        System.out.println("\t\t / * * \\");
        System.out.println("\t\t/ * * * \\");
        System.out.println("\t\t---------");
        System.out.println("\t\t\\ * * * /");
        System.out.println("\t\t \\ * * /");
        System.out.println("\t\t  \\ * /");
        System.out.println("\t\t   \\ /");
        System.out.println("\t\t    V");
    }
}
