package day07;

public class UnaryOperators {
    public static void main(String[] args) {

        int a = 12;
        a++;
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(a--);

        String str= "wooden";
        str += " spoon";
        System.out.println("str = " + str);





    }
}
